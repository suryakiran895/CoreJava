package BasicPrograms;
//Find out area and perimeter of the square.


class AreaS
{
	//Area of the Square: length * length

	public static void main(String args[])
	{
	int len=10;
	int area=len*len;					//area=10*10=100
	System.out.println("Area of the Square: "+area);	//Area of the Square: 100
	}
}

class PerimeterS
{
	//Perimeter of the Square: 4 * lentgh

	public static void main(String args[])
	{
	int len=8;		
	int perimeter=4*len;						//perimeter=4*8=32
	System.out.println("Perimeter of the Square: " +perimeter);	//Perimeter of the Square: 32 
	}
}